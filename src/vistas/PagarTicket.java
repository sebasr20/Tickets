    private void jbtnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPagarActionPerformed

        int idtick;
        try {
            efectivo = Integer.parseInt(this.jtxtDinero.getText());
            if (efectivo < totalPagar) {
                JOptionPane.showMessageDialog(this, "Efectivo Insuficiente", "Alerta", 0);
                this.jtxtDinero.requestFocus();
            } else {
                Ticket ticket = new Ticket(precioTicket, precioTicket, runCliente, null, idEstadio, idUbicacion, idEvento);
                bandera = true;
                RegistroTicket regticket = new RegistroTicket();

                if (regticket.agregar(ticket)) {
                    JOptionPane.showMessageDialog(this, "Pago Realizado Correctamente, gracias por su Compra", "Aviso", 1);

                } else {
                    JOptionPane.showMessageDialog(this, "No se registr√≥ el pago", "Aviso", 1);
                }
                this.jlblTotalPagar.setText("");
                this.jtxtDinero.setText("");
                mostrarPago();

                this.jlblNroTicket.setText(String.valueOf(obtenerIdTicket(runCliente,idEvento)));
                this.jtxtDinero.setEnabled(false);
                this.jbtnPagar.setEnabled(false);

            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un monto v√°lido", "Alerta", 1);
            this.jtxtDinero.requestFocus();
        }


    }//GEN-LAST:event_jbtnPagarActionPerformed

